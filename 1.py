b = float(input("Base: ")) #input base
h = float(input("Height: ")) #input height
Triangle_Area = (1/2)*b*h #formula of Triangle Area
print("Triangle Area = ", (Triangle_Area)) #display Triangle Area
pi = float(22/7) #pi
r = float(input("radius: ")) #input radius
Sphere_Volume = (4/3)*pi*(r**3) #formula of Sphere
print("Sphere Volume = ", (Sphere_Volume)) #display Sphere Volume
all = Triangle_Area ,Sphere_Volume
print(all)
